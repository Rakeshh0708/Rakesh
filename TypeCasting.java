package day1;

public class TypeCasting {
	public static void main(String[] args) {
		byte a=10;
		short b=a;
		int c=21;
		long d=c;
		double e=1.2;
		float f=(float)e;
		int x=60;
		byte y=(byte)x;
		System.out.println(a);
		System.out.println(b);
		System.out.println(f);

		System.out.println(y);
		
	}

}
